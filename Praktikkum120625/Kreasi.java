    private void tambahDigit(String digit) {
        if (resetDisplay) {
            txtDisplay.setText("0");
            resetDisplay = false;
        }
        
        String currentText = txtDisplay.getText();
        if (currentText.equals("0")) {
            txtDisplay.setText(digit);
        } else {
            txtDisplay.setText(currentText + digit);
        }
    }

