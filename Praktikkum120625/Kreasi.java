    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
        if (!operasi.isEmpty()) {
            angkaKedua = Double.parseDouble(txtDisplay.getText());
            double hasil = 0;
            
            switch (operasi) {
                case "+":
                    hasil = angkaPertama + angkaKedua;
                    break;
                case "-":
                    hasil = angkaPertama - angkaKedua;
                    break;
                case "*":
                    hasil = angkaPertama * angkaKedua;
                    break;
                case "/":
                    if (angkaKedua != 0) {
                        hasil = angkaPertama / angkaKedua;
                    } else {
                        txtDisplay.setText("Error");
                        return;
                    }
                    break;
            }
            
            // Tampilkan hasil
            if (hasil == (int) hasil) {
                txtDisplay.setText(String.valueOf((int) hasil));
            } else {
                txtDisplay.setText(String.valueOf(hasil));
            }
            
            operasi = "";
            resetDisplay = true;
        }
    }//GEN-LAST:event_btnEqualsActionPerformed

